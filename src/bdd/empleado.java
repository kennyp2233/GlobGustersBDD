    private void tablaEmpleados2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpleados2MouseClicked
        
        this.txtIDEmpleado2.setEnabled(false);
        this.jCheckBox1.setSelected(false);
        
        int filaSeleccionada = tablaEmpleados2.getSelectedRow();

        if (filaSeleccionada != -1) {
            // Se ha seleccionado una fila
            // Ahora puedes acceder a los datos de esa fila
            Object valorColumna1 = tablaEmpleados2.getValueAt(filaSeleccionada, 0); // Reemplaza 'columna1' con el nÃºmero de la columna que deseas obtener
            Object valorColumna2 = tablaEmpleados2.getValueAt(filaSeleccionada, 1); // Reemplaza 'columna2' con el nÃºmero de la otra columna que deseas obtener
            Object valorColumna3 = tablaEmpleados2.getValueAt(filaSeleccionada, 2); // Reemplaza 'columna2' con el nÃºmero de la otra columna que deseas obtener
            Object valorColumna4 = tablaEmpleados2.getValueAt(filaSeleccionada, 3); // Reemplaza 'columna2' con el nÃºmero de la otra columna que deseas obtener

            // ...

            
            this.txtIDEmpleado2.setText(valorColumna1.toString());
            this.txtNombreEmpleado2.setText(valorColumna2.toString());
            this.txtAgencia2.setText(valorColumna3.toString());
            this.txtIDAgencia2.setText(valorColumna4.toString());

        } else {
            // No se ha seleccionado ninguna fila
            System.out.println("Ninguna fila seleccionada");
        }
    }//GEN-LAST:event_tablaEmpleados2MouseClicked

