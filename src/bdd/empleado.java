    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int filaSeleccionada = tablaEmpleados3.getSelectedRow();
        
        if (filaSeleccionada != -1) {
            // Se ha seleccionado una fila
            // Ahora puedes acceder a los datos de esa fila
            Object valorColumna1 = tablaEmpleados3.getValueAt(filaSeleccionada, 0);
            Object valorColumna3 = tablaEmpleados3.getValueAt(filaSeleccionada, 2);
            
      
            
            try {
                consulta.eliminarEmpleado(valorColumna1.toString(), valorColumna3.toString());
            } catch (SQLException ex) {
                Logger.getLogger(empleado.class.getName()).log(Level.SEVERE, null, ex);
            }
      
        } else {
            // No se ha seleccionado ninguna fila
            System.out.println("Ninguna fila seleccionada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

